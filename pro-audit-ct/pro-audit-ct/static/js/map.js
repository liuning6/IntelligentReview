function initmap(s){
	//console.log('init:'+s);
	$('#map').html('').css({'background': 'url(/static/images/'+ s +'_0.png) no-repeat', 'background-size': '420.75px 680px', 'background-position': 'top center'});
	$('#map').append('<img data-src="'+STATIC+'/images/'+ s +'_0.png" src="'+STATIC+'/images/'+ s +'_0.png" usemap="#map" /><map name="map"></map>');
	if(ut == 3){
		$('#map img').attr('src', STATIC+'/images/'+ s + '_' + uo +'.png');
		return false;
	}
	as = $('#map map');
	$.each(coords[s], function(i, v){
		//as.before('<img class="'+ s +'_'+ i +'" src="'+STATIC+'/images/'+ s +'_'+ i +'.png" />');
		if(s == 0) n = tmp.place[i]; else n = tmp.station2[i];
		as.append('<area data-v="'+ s +'_'+ i +'" href="#'+ s +'_'+ i +'" shape="poly" coords="'+ setCoords(v) +'" onfocus="blur(this);" title="'+ n +'\n工单数：0\n合格率：-\n重复率：-" />');
	});
	$('#map').unbind().delegate('area', 'mouseover', function(){
		if(ut < 3) $('#map img').attr('src', STATIC+'/images/'+ $(this).data('v') +'.png');
	}).delegate('area', 'mouseout', function(){
		if(ut < 3) $('#map img').attr('src', $('#map img').data('src'));
	}).delegate('area', 'click', function(){
		if(dt < 3) ++dt;
		$('#fh').show();
		var ts = $(this).data('v').split('_')[1];
		if(tt == 0){
			$('#h0').text(tmp.place[ts]);
		}else if(tt == 1){
			$('#h0').text(tmp.station[ts][1]);
		}
		chart(1, tt, ts, dt);
		if(ut == 3 || tt > 0) return false;
		++tt;
		return false;
	});
}